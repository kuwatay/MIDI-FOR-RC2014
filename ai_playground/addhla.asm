        ; 初期値設定
        LD   HL, $1234         ; HLに$1234を設定
        LD   A, $F0            ; Aに$F0を設定

        ; Aレジスタの値をHLに加算
ADD_A_TO_HL:
        ADD  A, L              ; A + Lの結果をAに格納
        LD   L, A              ; 結果をLレジスタに格納
        LD   A, H              ; AにHレジスタの値をロード
